void matrix_scan_user(void) {
  if (is_cmd_tab_active) {
    if (timer_elapsed(cmd_tab_timer) > 1250) {
      unregister_code(KC_LCMD);
      is_cmd_tab_active = false;
    }
  }
}

void encoder_update_user(uint8_t index, bool clockwise) {
    switch (get_highest_layer(layer_state)) {
        case _MAIN:
            if (index == 0) {
                if (clockwise) {
                    tap_code(KC_WH_D);
                } else {
                    tap_code(KC_WH_U);
                }
            } else if (index == 1) {
                if (!is_cmd_tab_active) {
                    is_cmd_tab_active = true;
                    register_code(KC_LCMD);
                }
                cmd_tab_timer = timer_read();
                if (clockwise) {
                    tap_code16(S(KC_TAB));
                } else {
                    tap_code16(KC_TAB);
                }
            }
            break;
        case _BRACKETS:
            if (index == 0) {
                if (clockwise) {
                    tap_code16(VSCBCK);
                } else {
                    tap_code16(VSCFWD);
                }
            } else if (index == 1) {
                if (clockwise) {
                    tap_code16(S(KC_F3));
                } else {
                    tap_code(KC_F3);
                }
            }
            break;
         case _NAV:
            if (index == 0) {
                if (clockwise) {
                    tap_code(KC_MNXT);
                } else {
                    tap_code(KC_MPRV);
                }
            } else if (index == 1) {
                if (clockwise) {
                    tap_code(KC_VOLD);
                } else {
                    tap_code(KC_VOLU);
                }
            }
            break;
        default:
            break;

    }
}

layer_state_t layer_state_set_user(layer_state_t state) {
    switch (get_highest_layer(state)) {
    case _MAIN:
        rgblight_sethsv_noeeprom(HSV_RED);
        break;
    case _BRACKETS:
        rgblight_sethsv_noeeprom(HSV_GREEN);
        break;
    case _NAV:
        rgblight_sethsv_noeeprom(HSV_BLUE);
        break;
    case _ADJUST:
        rgblight_sethsv_noeeprom(HSV_PURPLE);
        break;
    default:
        break;
    }
  return state;
}
